    public function processCheckout(Request $request)
    {
        $cart = $request->cart;

        foreach ($cart as $item) {
            $product = Barang::find($item['id']);
            if ($product) {
                $product->stock += $item['qty'];
                $product->save();
            }
        }

        return response()->json([
            'success' => true,
            'message' => 'Transaksi berhasil!'
        ]);
    }


